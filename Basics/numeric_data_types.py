
#* Numeric Data Types:

# Integer - int
# whole number without fraction 

# Float
# whole number that contains decimals

# Complex
# number with a real and imaginary part

# Boolean
# represents either True or False

# Example usage:
x = 5  # integer
y = 3.14  # float
z = 2 + 3j  # complex
is_true = True  # boolean

print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'float'>
print(type(z))  # Output: <class 'complex'>
print(type(is_true))  # Output: <class 'bool'>